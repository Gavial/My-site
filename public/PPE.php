                <p><i><b>Le contexte :</b></i></p>
                        <p>Le projet PPE2, avait pour objectif l’amélioration de la qualité de service proposée par GEFOR à ses apprenants inscrits dans le site de Paris 1er. Pour se faire, il a été décidé de produire un cahier de texte accessible en ligne.</p>
                        <br>                    
                    <p><i><b>Dans le cadre de ce projet, j’ai réalisé les taches suivantes :</b></i></p>
                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 1) Proposer un formulaire permettant de saisir un identifiant/mot de passe, pour la connexion au service cahier de texte.</p>
                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 2) Proposer un programme CGI de gestion des connexions au service. Les identifiants/mots de passe, sont stockés cryptés dans un fichier texte respectant le format: indentifiant:mot-de-passe-crypté:nom:prénom:groupe:date-creation</p>
                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 3) Proposer au moins deux méthodes d'implémentation possibles pour la réalisation d'un FAQ</p>
                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 4) Rédiger un argumentaire synthétique permettant de répondre à ces questions : 
                        <br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp A) Peut-on utiliser un CMS pour la réalisation du cahier de texte en ligne?
                        <br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp B) Quelle est la différence entre WordPress et SPIP et quel est à votre avis celui qui conviendrait le mieux?</p>
                        <br>
                    <p><i><b>Ces taches se sont déroulées comme suit :</b></i></p>
                        <p><u><i>Tache 1</i></u></p>
                        <p>Le formulaire permettant de saisir l’identifiant/mot de passe a été réalisé avec l’environnement de développement proposé par SublimeText. Les langages utilisés pour le développement sont : HTML pour le corps du formulaire et CSS pour la mise en forme.</p>
                        <br>
                        <p><u><i>Tache 2</i></u></p>
                        <p>Le programme CGI permettant de gérer les connexions au service a été réalisé en PhP. Il  comporte un cryptage des identifiants ainsi que des mots de passe, et stock les informations dans un fichier texte respectant le format spécifié. </p>
                        <br>
                        <p><u><i>Tache 3</i></u></p>
                        <p>L’étude réalisée afin de proposer au moins deux méthodes d'implémentation possibles pour la réalisation d'un FAQ a été réalisée avec l’ensemble des étudiants.</p>
                        <br>
                        <p><u><i>Tache 4</i></u></p>
                        <p>Les recherches réalisées afin de proposer un argumentaire répondant aux deux questions posées ont été réalisées avec l’ensemble des étudiants.</p>
                        <br>
                    <p><i><b>Contraintes (matérielles, logicielles, humaines, financières) :</b></i></p>
                        <p>La contrainte liée à la réalisation de nos lots de ce projet fut : La dead line au 31/10/2014, or ce projet a nécessité une part importante de temps consacrée à l’étude afin d’approfondir nos connaissances, notamment via des recherches concernant les langages, techniques et possibilités existantes de cryptage de données ainsi que la conception et l’insertion du fichier texte qui stock les informations renseignées dans le formulaire.</p>
                        <br>
                    <p><i><b>Résultat de l’activité :</b></i></p>
                        <p>L’ensemble des missions ont été réalisées dans les temps et répondent au cahier des charges.</p>
                        <br>
                    <p><i><b>Impact du nouveau service sur l’organisation cliente (financier, organisationnel) :</b></i></p>
                        <p>Ce projet va permettre de : limiter le risque d’erreurs, libérer du temps de travail, rendre le service accessible 24h/24h, possibilité d’analyse des comportements (nombre de connexion, fréquence, pourcentage des connexions hors des horaires de bureau….), et améliorer la satisfaction de l’utilisateur.</p>
                        <br>
                    <p><i><b>Analyse critique sur la démarche et les résultats :</b></i></p>
                        <p>Ce projet a été réalisé avec un fort esprit d’équipe et une entre aide importante. Les moyens techniques mis à notre disposition étaient en corrélation avec nos besoins. La date limite a été respectée, et les missions ont été affectées de façon homogène. Le résultat obtenu correspond à l’attente de nos enseignants. Les recherches et veille technologique liées aux missions de chacun ont permis un accroissement de connaissances particulièrement intéressantes. Les outils collaboratifs mis en place, google drive, agenda partagé et boite mail commune, ont permis une optimisation des échanges entre les membres de l’équipe et une réactivité notamment pour les tâches dépendantes les unes des autres.</p>
